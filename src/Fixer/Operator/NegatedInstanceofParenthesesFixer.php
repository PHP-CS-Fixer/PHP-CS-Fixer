namespace PhpCsFixer\Fixer\Operator;

use PhpCsFixer\AbstractFixer;
use PhpCsFixer\Fixer\ConfigurableFixerInterface;
use PhpCsFixer\Fixer\ConfigurableFixerTrait;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolver;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolverInterface;
use PhpCsFixer\FixerConfiguration\FixerOptionBuilder;
use PhpCsFixer\FixerDefinition\CodeSample;
use PhpCsFixer\FixerDefinition\FixerDefinition;
use PhpCsFixer\FixerDefinition\FixerDefinitionInterface;
use PhpCsFixer\Tokenizer\CT;
use PhpCsFixer\Tokenizer\Token;
use PhpCsFixer\Tokenizer\Tokens;

/**
 * @phpstan-type _AutogeneratedInputConfiguration array{
 *  use_parentheses?: bool,
 * }
 * @phpstan-type _AutogeneratedComputedConfiguration array{
 *  use_parentheses: bool,
 * }
 *
 * @implements ConfigurableFixerInterface<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration>
 *
 * @author Jérôme Gamez <jerome@gamez.name>
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class NegatedInstanceofParenthesesFixer extends AbstractFixer implements ConfigurableFixerInterface
{
    /** @use ConfigurableFixerTrait<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration> */
    use ConfigurableFixerTrait;

    /** @var list<int> */
    private array $skipTokens = [
        CT::T_NAMESPACE_OPERATOR,
        \T_DOUBLE_COLON,
        \T_NS_SEPARATOR,
        \T_OBJECT_OPERATOR,
        \T_STATIC,
        \T_STRING,
        \T_VARIABLE,
    ];

    public function getDefinition(): FixerDefinitionInterface
    {
        return new FixerDefinition(
            'Negated `instanceof` expressions must (not) be wrapped in parentheses.',
            [
                new CodeSample(
                    "<?php\n!(\$foo instanceof Foo);\n",
                ),
                new CodeSample(
                    "<?php\n!\$foo instanceof Foo;\n",
                    ['use_parentheses' => true],
                ),
            ],
        );
    }

    public function isCandidate(Tokens $tokens): bool
    {
        return $tokens->isTokenKindFound(\T_INSTANCEOF);
    }

    public function createConfigurationDefinition(): FixerConfigurationResolverInterface
    {
        return new FixerConfigurationResolver([
            (new FixerOptionBuilder('use_parentheses', 'Whether negated `instanceof` expressions should be wrapped in parentheses or not.'))
                ->setAllowedTypes(['bool'])
                ->setDefault(false)
                ->getOption(),
        ]);
    }

    protected function applyFix(\SplFileInfo $file, Tokens $tokens): void
    {
        $useParentheses = $this->configuration['use_parentheses'];

        for ($index = $tokens->count() - 1; $index >= 0; --$index) {
            if (!$tokens[$index]->isGivenKind(\T_INSTANCEOF)) {
                continue;
            }

            [$start, $end] = $this->findEdges($tokens, $index);
            $end ??= $this->findEnd($tokens, $index);

            $hasParentheses = $tokens[$start]->equals('(') && $tokens[$end]->equals(')');
            $isNegated = $tokens[$tokens->getPrevMeaningfulToken($start)]->equals('!');

            if (!$isNegated) {
                continue;
            }

            if ($useParentheses === $hasParentheses) {
                continue;
            }

            if ($useParentheses) {
                $this->addParentheses($tokens, $start, $end);
            } else {
                $this->removeParentheses($tokens, $start, $end);
            }
        }
    }

    /**
     * @return array{0: int, 1: ?int}
     */
    private function findEdges(Tokens $tokens, int $instanceofIndex): array
    {
        $currentIndex = $instanceofIndex;

        while (($prev = $tokens->getPrevMeaningfulToken($currentIndex)) !== null) {
            $type = Tokens::detectBlockType($tokens[$prev]);

            if (null !== $type && false === $type['isStart']) {
                $currentIndex = $tokens->findBlockStart($type['type'], $prev);

                continue;
            }

            if (null !== $type && true === $type['isStart'] && Tokens::BLOCK_TYPE_PARENTHESIS_BRACE === $type['type']) {
                return [$prev, $tokens->findBlockEnd($type['type'], $prev)];
            }

            if ($tokens[$prev]->isGivenKind($this->skipTokens)) {
                $currentIndex = $prev;

                continue;
            }

            break;
        }

        return [$currentIndex, null];
    }

    private function findEnd(Tokens $tokens, int $index): int
    {
        while (($next = $tokens->getNextMeaningfulToken($index)) !== null) {
            $type = Tokens::detectBlockType($tokens[$next]);

            if (null !== $type && $type['isStart']) {
                $index = $tokens->findBlockEnd($type['type'], $next);

                continue;
            }

            if ($tokens[$next]->isGivenKind($this->skipTokens)) {
                $index = $next;

                continue;
            }

            break;
        }

        return $index;
    }

    private function addParentheses(Tokens $tokens, int $startIndex, int $endIndex): void
    {
        // Adding parentheses directly before the left operand and after the right operand would work in most
        // cases, but would lead to unexpected output in some edge cases with comments, e.g.
        //
        // if (!/* comment */$foo instanceof Foo/* comment */) {};
        // would become
        // if (!/* comment */($foo instanceof Foo)/* comment */) {};
        //
        // To avoid this, we add the opening parenthesis right after the previous meaningful token (which we know
        // is a `!` in this case) and the closing parenthesis right before the next meaningful token:
        //
        // if (!(/* comment */$foo instanceof Foo/* comment */)) {};

        $openingIndex = $tokens->getPrevMeaningfulToken($startIndex);
        $openingIndex = null !== $openingIndex ? $openingIndex + 1 : $startIndex;

        $closingIndex = $tokens->getNextMeaningfulToken($endIndex);
        $closingIndex = null !== $closingIndex ? $closingIndex : $endIndex + 1;

        // However, this alone could lead to unwanted formatting in other cases:
        //
        // !$x instanceof Foo && !$y instanceof Bar;
        // would become
        // !($x instanceof Foo )&& !($y instanceof Bar);
        //
        // To prevent that, we need to put the closing parenthesis before any whitespace
        // preceding the next meaningful token

        if ($tokens[$closingIndex - 1]->isWhitespace()) {
            $closingIndex = $tokens->getPrevNonWhitespace($closingIndex) + 1;
        }

        // So far so good, but there is still one edge case remaining: `//` comments
        //
        // if (
        //     !$x instanceof Foo // comment
        // ) {};
        //
        // would become
        //
        // if (
        //     !($x instanceof Foo // comment)
        // ) {};
        //
        // which is invalid, because the `//` comment comments out the closing parenthesis
        // To fix that, we need to put the closing parenthesis before the next meaningful token
        $potentialCommentIndex = $closingIndex - 1;

        if (
            $tokens[$potentialCommentIndex]->isComment()
            && str_starts_with($tokens[$potentialCommentIndex]->getContent(), '//')
        ) {
            $nextIndex = $tokens->getNextMeaningfulToken($endIndex);

            if (null !== $nextIndex) {
                $closingIndex = $nextIndex;
            }
        }

        $tokens->insertSlices([
            $openingIndex => new Token('('),
            $closingIndex => new Token(')'),
        ]);
    }

    private function removeParentheses(Tokens $tokens, int $startIndex, int $endIndex): void
    {
        $tokens->clearTokenAndMergeSurroundingWhitespace($endIndex);
        $tokens->clearTokenAndMergeSurroundingWhitespace($startIndex);
    }
}
