namespace PhpCsFixer\Fixer\Operator;

use PhpCsFixer\AbstractFixer;
use PhpCsFixer\Fixer\ConfigurableFixerInterface;
use PhpCsFixer\Fixer\ConfigurableFixerTrait;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolver;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolverInterface;
use PhpCsFixer\FixerConfiguration\FixerOptionBuilder;
use PhpCsFixer\FixerDefinition\CodeSample;
use PhpCsFixer\FixerDefinition\FixerDefinition;
use PhpCsFixer\FixerDefinition\FixerDefinitionInterface;
use PhpCsFixer\Tokenizer\Token;
use PhpCsFixer\Tokenizer\Tokens;
use PhpCsFixer\Tokenizer\TokensAnalyzer;
use PhpCsFixer\Utils;
use Symfony\Component\OptionsResolver\Exception\InvalidOptionsException;

/**
 * @phpstan-type _AutogeneratedInputConfiguration array{
 *  default_strategy?: 'after'|'around'|'before'|null,
 *  operators?: array<string, ?string>,
 * }
 * @phpstan-type _AutogeneratedComputedConfiguration array{
 *  default_strategy: 'after'|'around'|'before'|null,
 *  operators: array<string, ?string>,
 * }
 *
 * @implements ConfigurableFixerInterface<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration>
 *
 * @author Maksim Spirkov <spirkov.2001@mail.ru>
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class NoLineBreakNearBinaryOperatorFixer extends AbstractFixer implements ConfigurableFixerInterface
{
    /** @use ConfigurableFixerTrait<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration> */
    use ConfigurableFixerTrait;

    /**
     * @internal
     */
    public const BEFORE = 'before';

    /**
     * @internal
     */
    public const AFTER = 'after';

    /**
     * @internal
     */
    public const AROUND = 'around';

    /**
     * @var non-empty-list<null|string>
     */
    private const ALLOWED_VALUES = [
        self::BEFORE,
        self::AFTER,
        self::AROUND,
        null,
    ];

    private TokensAnalyzer $tokensAnalyzer;

    /**
     * @var array<string, ?string>
     */
    private array $operators = [];

    public function getDefinition(): FixerDefinitionInterface
    {
        return new FixerDefinition(
            'Removes line breaks around binary operators according to the configuration.',
            [
                new CodeSample(
                    <<<'PHP'
                        <?php
                        $var1
                            = [
                                'foo' => 'bar',
                                'bar' => 'baz',
                            ];

                        $var2
                            ??= new stdClass();

                        $var3 =
                        [
                            'foo'
                                => 'bar',
                            'bar'
                                => 'baz',
                        ];

                        $var4 = 'Some text'
                            . 'Second text';

                        PHP,
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        $var1
                            = [
                                'foo' => 'bar',
                                'bar' => 'baz',
                            ];

                        $var2
                            ??= new stdClass();

                        $var3 =
                        [
                            'foo'
                                => 'bar',
                            'bar'
                                => 'baz',
                        ];

                        $var4 = 'Some text'
                            . 'Second text';

                        PHP,
                    ['default_strategy' => self::BEFORE],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        $var1
                            = [
                                'foo' => 'bar',
                                'bar' => 'baz',
                            ];

                        $var2
                            ??= new stdClass();

                        $var3 =
                        [
                            'foo'
                                => 'bar',
                            'bar'
                                => 'baz',
                        ];

                        $var4 = 'Some text'
                            . 'Second text';

                        PHP,
                    ['default_strategy' => self::AROUND],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        $var1 = [
                                'foo' => 'bar',
                                'bar' => 'baz',
                            ];

                        $var2
                            ??= new stdClass();

                        $var3 =
                        [
                            'foo'
                                => 'bar',
                            'bar'
                                => 'baz',
                        ];

                        $var4 = 'Some text'
                            . 'Second text';

                        PHP,
                    ['operators' => ['=' => self::AROUND]],
                ),
            ],
        );
    }

    /**
     * {@inheritdoc}
     *
     * Must run before BinaryOperatorSpacesFixer, MethodArgumentSpaceFixer.
     * Must run after OperatorLinebreakFixer.
     */
    public function getPriority(): int
    {
        return 31;
    }

    public function isCandidate(Tokens $tokens): bool
    {
        return true;
    }

    protected function configurePostNormalisation(): void
    {
        $this->operators = array_map(
            static fn (?string $strategy) => null !== $strategy ? strtolower($strategy) : null,
            $this->configuration['operators'],
        );
    }

    protected function applyFix(\SplFileInfo $file, Tokens $tokens): void
    {
        $this->tokensAnalyzer = new TokensAnalyzer($tokens);

        // last and first tokens cannot be an operator
        for ($index = $tokens->count() - 2; $index > 0; --$index) {
            if (!$this->tokensAnalyzer->isBinaryOperator($index)) {
                continue;
            }

            $operator = strtolower($tokens[$index]->getContent());
            $operatorStrategy = null;

            if (\array_key_exists($operator, $this->operators)) {
                $operatorStrategy = $this->operators[$operator];
                // If the strategy for the operator is explicitly specified as `null`, then we skip this operator
                if (null === $operatorStrategy) {
                    continue;
                }
            }

            $operatorStrategy ??= $this->configuration['default_strategy'];
            if (null === $operatorStrategy) {
                continue;
            }

            if (\in_array($operatorStrategy, [self::BEFORE, self::AROUND], true)) {
                if (!$tokens[$index - 2]->isComment() || str_starts_with($tokens[$index - 2]->getContent(), '/*')) {
                    $this->removeLineBreak($tokens, $index - 1);
                }
            }

            if (\in_array($operatorStrategy, [self::AFTER, self::AROUND], true)) {
                if (!$tokens[$index + 2]->isComment()) {
                    $this->removeLineBreak($tokens, $index + 1);
                }
            }

            --$index;
        }
    }

    protected function createConfigurationDefinition(): FixerConfigurationResolverInterface
    {
        return new FixerConfigurationResolver([
            (new FixerOptionBuilder('default_strategy', 'Default fix strategy.'))
                ->setDefault(self::AFTER)
                ->setAllowedValues(self::ALLOWED_VALUES)
                ->getOption(),
            (new FixerOptionBuilder('operators', 'Dictionary of `binary operator` => `fix strategy` values that differ from the default strategy.'))
                ->setAllowedTypes(['array<string, ?string>'])
                ->setAllowedValues([static function (array $option): bool {
                    foreach ($option as $operator => $value) {
                        if (!\in_array($value, self::ALLOWED_VALUES, true)) {
                            throw new InvalidOptionsException(
                                \sprintf(
                                    'Unexpected value for operator "%s", expected any of %s, got "%s".',
                                    $operator,
                                    Utils::naturalLanguageJoin(array_map(
                                        static fn ($value): string => Utils::toString($value),
                                        self::ALLOWED_VALUES,
                                    )),
                                    \is_object($value) ? \get_class($value) : (null === $value ? 'null' : \gettype($value).'#'.$value),
                                ),
                            );
                        }
                    }

                    return true;
                }])
                ->setDefault([])
                ->getOption(),
        ]);
    }

    private function removeLineBreak(Tokens $tokens, int $index): void
    {
        $token = $tokens[$index];

        if ($token->isWhitespace() && !$token->isWhitespace(" \t")) {
            $tokens[$index] = new Token([\T_WHITESPACE, rtrim($token->getContent()).' ']);
        }
    }
}
