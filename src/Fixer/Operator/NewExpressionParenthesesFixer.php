namespace PhpCsFixer\Fixer\Operator;

use PhpCsFixer\AbstractFixer;
use PhpCsFixer\Fixer\ConfigurableFixerInterface;
use PhpCsFixer\Fixer\ConfigurableFixerTrait;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolver;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolverInterface;
use PhpCsFixer\FixerConfiguration\FixerOptionBuilder;
use PhpCsFixer\FixerDefinition\CodeSample;
use PhpCsFixer\FixerDefinition\FixerDefinition;
use PhpCsFixer\FixerDefinition\FixerDefinitionInterface;
use PhpCsFixer\Tokenizer\CT;
use PhpCsFixer\Tokenizer\Tokens;

/**
 * @author Valentin Udaltsov <udaltsov.valentin@gmail.com>
 *
 * @implements ConfigurableFixerInterface<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration>
 *
 * @phpstan-type _AutogeneratedInputConfiguration array{
 *  anonymous_class?: bool|null,
 *  named_class?: bool|null
 * }
 * @phpstan-type _AutogeneratedComputedConfiguration array{
 *  anonymous_class: bool|null,
 *  named_class: bool|null
 * }
 */
final class NewExpressionParenthesesFixer extends AbstractFixer implements ConfigurableFixerInterface
{
    /** @use ConfigurableFixerTrait<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration> */
    use ConfigurableFixerTrait;

    public function getDefinition(): FixerDefinitionInterface
    {
        return new FixerDefinition(
            'All `new` expressions must (not) be wrapped in parentheses upon access.',
            [
                // TODO add more
                new CodeSample("<?php\n\n(new Foo())->bar;\n"),
            ]
        );
    }

    /**
     * {@inheritdoc}
     *
     * Must run before ClassDefinitionFixer.
     */
    public function getPriority(): int
    {
        return 37;
    }

    public function isCandidate(Tokens $tokens): bool
    {
        return \PHP_VERSION_ID >= 8_04_00 && $tokens->isTokenKindFound(T_NEW);
    }

    /** @protected */
    public function createConfigurationDefinition(): FixerConfigurationResolverInterface
    {
        return new FixerConfigurationResolver([
            (new FixerOptionBuilder('named_class', 'Whether new named class expressions should be wrapped in parentheses.'))
                ->setAllowedTypes(['bool', 'null'])
                ->setDefault(false)
                ->getOption(),
            (new FixerOptionBuilder('anonymous_class', 'Whether new anonymous class expressions should be wrapped in parentheses.'))
                ->setAllowedTypes(['bool', 'null'])
                ->setDefault(false)
                ->getOption(),
        ]);
    }

    protected function applyFix(\SplFileInfo $file, Tokens $tokens): void
    {
        foreach ($tokens as $index => $token) {
            if (!$token->isGivenKind(T_NEW)) {
                continue;
            }

            $exprOpenParenthesisIndex = $tokens->getPrevMeaningfulToken($index);

            if (!$tokens[$exprOpenParenthesisIndex]->isGivenKind(CT::T_BRACE_CLASS_INSTANTIATION_OPEN)) {
                continue;
            }

            $classStartIndex = $tokens->getNextMeaningfulToken($index);

            // anonymous class
            if ($tokens[$classStartIndex]->isGivenKind(T_CLASS)) {
                $argsOpenParenthesisIndex = $tokens->getNextMeaningfulToken($classStartIndex);

                if ($tokens[$argsOpenParenthesisIndex]->equals('(')) {
                    $classStartIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS_BRACE, $argsOpenParenthesisIndex);
                }

                $anonymousStartIndex = $tokens->getNextTokenOfKind($classStartIndex, ['{']);
                $anonymousEndIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_CURLY_BRACE, $anonymousStartIndex);

                $exprCloseParenthesisIndex = $tokens->getNextMeaningfulToken($anonymousEndIndex);

                if (!$tokens[$exprCloseParenthesisIndex]->isGivenKind(CT::T_BRACE_CLASS_INSTANTIATION_CLOSE)) {
                    continue;
                }

                $tokens->clearAt($exprOpenParenthesisIndex);
                $tokens->clearAt($exprCloseParenthesisIndex);

                continue;
            }

            $classNameEndIndex = $this->findClassNameEndIndex($tokens, $classStartIndex);

            if (null === $classNameEndIndex) {
                continue;
            }

            $argsOpenParenthesisIndex = $tokens->getNextMeaningfulToken($classNameEndIndex);

            if (!$tokens[$argsOpenParenthesisIndex]->equals('(')) {
                continue;
            }

            $argsCloseParenthesisIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS_BRACE, $argsOpenParenthesisIndex);
            $exprCloseParenthesisIndex = $tokens->getNextMeaningfulToken($argsCloseParenthesisIndex);

            if (!$tokens[$exprCloseParenthesisIndex]->isGivenKind(CT::T_BRACE_CLASS_INSTANTIATION_CLOSE)) {
                continue;
            }

            $tokens->clearAt($exprOpenParenthesisIndex);
            $tokens->clearAt($exprCloseParenthesisIndex);
        }
    }

    private function findClassNameEndIndex(Tokens $tokens, ?int $startIndex): ?int
    {
        // class name
        if ($tokens[$startIndex]->isGivenKind([T_STRING, T_NS_SEPARATOR])) {
            return $tokens->getTokenNotOfKindsSibling($startIndex, 1, [T_STRING, T_NS_SEPARATOR]) - 1;
        }

        // variable class name
        if ($tokens[$startIndex]->isGivenKind(T_VARIABLE)) {
            return $startIndex;
        }

        // (expression) class name
        if ($tokens[$startIndex]->equals('(')) {
            return $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS_BRACE, $startIndex);
        }

        return null;
    }
}
