namespace PhpCsFixer\Fixer\Whitespace;

use PhpCsFixer\AbstractFixer;
use PhpCsFixer\Fixer\ConfigurableFixerInterface;
use PhpCsFixer\Fixer\ConfigurableFixerTrait;
use PhpCsFixer\Fixer\WhitespacesAwareFixerInterface;
use PhpCsFixer\FixerConfiguration\AllowedValueSubset;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolver;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolverInterface;
use PhpCsFixer\FixerConfiguration\FixerOptionBuilder;
use PhpCsFixer\FixerDefinition\CodeSample;
use PhpCsFixer\FixerDefinition\FixerDefinition;
use PhpCsFixer\FixerDefinition\FixerDefinitionInterface;
use PhpCsFixer\Tokenizer\Token;
use PhpCsFixer\Tokenizer\Tokens;
use PhpCsFixer\Tokenizer\TokensAnalyzer;

/**
 * @phpstan-type _AutogeneratedInputConfiguration array{
 *  statements?: list<'declare'|'do'|'for'|'foreach'|'if'|'switch'|'try'|'while'>,
 * }
 * @phpstan-type _AutogeneratedComputedConfiguration array{
 *  statements: list<'declare'|'do'|'for'|'foreach'|'if'|'switch'|'try'|'while'>,
 * }
 *
 * @implements ConfigurableFixerInterface<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration>
 *
 * @author Andreas Möller <am@localheinz.com>
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class BlankLineAfterStatementFixer extends AbstractFixer implements ConfigurableFixerInterface, WhitespacesAwareFixerInterface
{
    /** @use ConfigurableFixerTrait<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration> */
    use ConfigurableFixerTrait;

    /**
     * @var array<string, int>
     */
    private const TOKEN_MAP = [
        'declare' => \T_DECLARE,
        'do' => \T_DO,
        'for' => \T_FOR,
        'foreach' => \T_FOREACH,
        'if' => \T_IF,
        'switch' => \T_SWITCH,
        'try' => \T_TRY,
        'while' => \T_WHILE,
    ];

    /**
     * @var list<int>
     */
    private array $fixTokenMap = [];

    public function getDefinition(): FixerDefinitionInterface
    {
        return new FixerDefinition(
            'An empty line feed must follow any configured statement.',
            [
                new CodeSample(
                    <<<'PHP'
                        <?php

                        declare(strict_types=1);
                        use Foo\Bar;

                        return [];

                        PHP,
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        if (true) {
                            $a = 1;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['if'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        switch ($a) {
                            case 1:
                                break;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['switch'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        for ($i = 0; $i < 3; ++$i) {
                            $a = $i;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['for'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        foreach ($arr as $v) {
                            $a = $v;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['foreach'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        while ($a) {
                            --$a;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['while'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        do {
                            --$a;
                        } while ($a > 0);
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['do'],
                    ],
                ),
                new CodeSample(
                    <<<'PHP'
                        <?php
                        try {
                            $a = 1;
                        } catch (\Throwable $t) {
                            $a = 0;
                        }
                        $b = 2;

                        PHP,
                    [
                        'statements' => ['try'],
                    ],
                ),
            ],
        );
    }

    /**
     * {@inheritdoc}
     *
     * Must run after DeclareStrictTypesFixer, FullyQualifiedStrictTypesFixer, NoUselessElseFixer.
     */
    public function getPriority(): int
    {
        return -21;
    }

    public function isCandidate(Tokens $tokens): bool
    {
        return $tokens->isAnyTokenKindsFound($this->fixTokenMap);
    }

    protected function configurePostNormalisation(): void
    {
        $fixTokenMap = [];

        foreach ($this->configuration['statements'] as $key) {
            $fixTokenMap[$key] = self::TOKEN_MAP[$key];
        }

        $this->fixTokenMap = array_values($fixTokenMap);
    }

    protected function applyFix(\SplFileInfo $file, Tokens $tokens): void
    {
        $analyzer = new TokensAnalyzer($tokens);

        for ($index = $tokens->count() - 1; $index > 0; --$index) {
            $token = $tokens[$index];

            if (!$token->isGivenKind($this->fixTokenMap)) {
                continue;
            }

            if ($token->isGivenKind(\T_WHILE) && $analyzer->isWhilePartOfDoWhile($index)) {
                continue;
            }

            $endIndex = $this->findStatementEnd($tokens, $index);

            if (null === $endIndex) {
                continue;
            }

            $nextMeaningfulIndex = $tokens->getNextMeaningfulToken($endIndex);

            if (null === $nextMeaningfulIndex) {
                continue;
            }

            if ($tokens[$nextMeaningfulIndex]->equals('}')) {
                continue;
            }

            $insertAfterIndex = $this->skipTrailingInlineComment($tokens, $endIndex);

            $this->ensureBlankLineAfter($tokens, $insertAfterIndex);
        }
    }

    protected function createConfigurationDefinition(): FixerConfigurationResolverInterface
    {
        return new FixerConfigurationResolver([
            (new FixerOptionBuilder('statements', 'List of statements which must be followed by an empty line.'))
                ->setAllowedTypes(['string[]'])
                ->setAllowedValues([new AllowedValueSubset(array_keys(self::TOKEN_MAP))])
                ->setDefault([
                    'declare',
                ])
                ->getOption(),
        ]);
    }

    /**
     * Adapted from ErickSkrauch's LineBreakAfterStatementsFixer.
     *
     * @see https://github.com/erickskrauch/php-cs-fixer-custom-fixers/blob/1.3.1/src/Fixer/Whitespace/LineBreakAfterStatementsFixer.php
     *
     * Returns null when the construct uses alternative syntax (e.g. `if: ... endif;`), which is
     * currently out of scope — the caller should skip such constructs.
     */
    private function findStatementEnd(Tokens $tokens, int $index): ?int
    {
        $nextIndex = $tokens->getNextMeaningfulToken($index);

        if (null === $nextIndex) {
            return $index;
        }

        $nextToken = $tokens[$nextIndex];

        if ($nextToken->equals('(')) {
            $parenthesisEndIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS_BRACE, $nextIndex);
            $possibleBeginBraceIndex = $tokens->getNextNonWhitespace($parenthesisEndIndex);
        } else {
            $possibleBeginBraceIndex = $nextIndex;
        }

        if (null === $possibleBeginBraceIndex) {
            return $nextIndex;
        }

        $possibleBeginBrace = $tokens[$possibleBeginBraceIndex];

        if ($possibleBeginBrace->equals(':')) {
            return null;
        }

        if ($possibleBeginBrace->equals(';')) {
            $blockEnd = $possibleBeginBraceIndex;
        } elseif ($possibleBeginBrace->equals('{')) {
            $blockEnd = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_CURLY_BRACE, $possibleBeginBraceIndex);
        } else {
            $blockEnd = $tokens->getNextTokenOfKind($possibleBeginBraceIndex, [';']);

            if (null === $blockEnd) {
                return $possibleBeginBraceIndex;
            }
        }

        if ($tokens[$index]->isGivenKind(\T_DO)) {
            $whileIndex = $tokens->getNextMeaningfulToken($blockEnd);

            if (null !== $whileIndex && $tokens[$whileIndex]->isGivenKind(\T_WHILE)) {
                $conditionStartIndex = $tokens->getNextMeaningfulToken($whileIndex);

                if (null !== $conditionStartIndex && $tokens[$conditionStartIndex]->equals('(')) {
                    $conditionEndIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS_BRACE, $conditionStartIndex);
                    $semicolonIndex = $tokens->getNextTokenOfKind($conditionEndIndex, [';']);

                    if (null !== $semicolonIndex) {
                        return $semicolonIndex;
                    }
                }
            }

            return $blockEnd;
        }

        $nextStatementIndex = $tokens->getNextMeaningfulToken($blockEnd);

        if (null === $nextStatementIndex) {
            return $blockEnd;
        }

        if ($tokens[$nextStatementIndex]->isGivenKind(\T_ELSEIF)) {
            return $this->findStatementEnd($tokens, $nextStatementIndex);
        }

        if ($tokens[$nextStatementIndex]->isGivenKind(\T_ELSE)) {
            $nextNextStatementIndex = $tokens->getNextMeaningfulToken($nextStatementIndex);

            if (null !== $nextNextStatementIndex && $tokens[$nextNextStatementIndex]->isGivenKind(\T_IF)) {
                return $this->findStatementEnd($tokens, $nextNextStatementIndex);
            }

            return $this->findStatementEnd($tokens, $nextStatementIndex);
        }

        if ($tokens[$nextStatementIndex]->isGivenKind([\T_CATCH, \T_FINALLY])) {
            return $this->findStatementEnd($tokens, $nextStatementIndex);
        }

        return $blockEnd;
    }

    /**
     * Advances past any trailing inline comment that sits on the same line as the statement's
     * closing token, so that the blank line is inserted after the comment rather than between
     * the closing token and the comment.
     */
    private function skipTrailingInlineComment(Tokens $tokens, int $endIndex): int
    {
        $whitespaceIndex = $endIndex + 1;

        if (!isset($tokens[$whitespaceIndex]) || !$tokens[$whitespaceIndex]->isWhitespace()) {
            return $endIndex;
        }

        if (str_contains($tokens[$whitespaceIndex]->getContent(), "\n")) {
            return $endIndex;
        }

        $commentIndex = $whitespaceIndex + 1;

        if (!isset($tokens[$commentIndex]) || !$tokens[$commentIndex]->isComment()) {
            return $endIndex;
        }

        return $commentIndex;
    }

    private function ensureBlankLineAfter(Tokens $tokens, int $index): void
    {
        $nextIndex = $index + 1;

        if (!isset($tokens[$nextIndex])) {
            return;
        }

        $nextToken = $tokens[$nextIndex];
        $lineEnding = $this->whitespacesConfig->getLineEnding();

        if ($nextToken->isWhitespace()) {
            $newlinesCount = substr_count($nextToken->getContent(), "\n");

            if (0 === $newlinesCount) {
                $tokens[$nextIndex] = new Token([\T_WHITESPACE, $lineEnding.$lineEnding.ltrim($nextToken->getContent(), " \t")]);
            } elseif (1 === $newlinesCount) {
                $tokens[$nextIndex] = new Token([\T_WHITESPACE, $lineEnding.$nextToken->getContent()]);
            }
        } else {
            $tokens->insertAt($nextIndex, new Token([\T_WHITESPACE, $lineEnding.$lineEnding]));
        }
    }
}
