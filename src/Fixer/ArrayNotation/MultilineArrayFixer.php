namespace PhpCsFixer\Fixer\ArrayNotation;

use PhpCsFixer\AbstractFixer;
use PhpCsFixer\Fixer\ConfigurableFixerInterface;
use PhpCsFixer\Fixer\ConfigurableFixerTrait;
use PhpCsFixer\Fixer\WhitespacesAwareFixerInterface;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolver;
use PhpCsFixer\FixerConfiguration\FixerConfigurationResolverInterface;
use PhpCsFixer\FixerConfiguration\FixerOptionBuilder;
use PhpCsFixer\FixerDefinition\CodeSample;
use PhpCsFixer\FixerDefinition\FixerDefinition;
use PhpCsFixer\FixerDefinition\FixerDefinitionInterface;
use PhpCsFixer\Tokenizer\CT;
use PhpCsFixer\Tokenizer\Tokens;

/**
 * @phpstan-type _AutogeneratedInputConfiguration array{
 *  min_items?: int,
 *  variables_only?: bool,
 * }
 * @phpstan-type _AutogeneratedComputedConfiguration array{
 *  min_items: int,
 *  variables_only: bool,
 * }
 *
 * @implements ConfigurableFixerInterface<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration>
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class MultilineArrayFixer extends AbstractFixer implements ConfigurableFixerInterface, WhitespacesAwareFixerInterface
{
    /** @use ConfigurableFixerTrait<_AutogeneratedInputConfiguration, _AutogeneratedComputedConfiguration> */
    use ConfigurableFixerTrait;

    public function getDefinition(): FixerDefinitionInterface
    {
        return new FixerDefinition(
            'Arrays with at least a configured number of elements MUST be split across multiple lines, with one element per line.',
            [
                new CodeSample("<?php\n\$foo = ['a', 'b', 'c'];\n"),
                new CodeSample(
                    "<?php\n\$foo = ['a', 'b'];\n\$bar = ['a', 'b', 'c'];\n",
                    ['min_items' => 3],
                ),
                new CodeSample(
                    "<?php\nbar(['a', 'b']);\n\$foo = ['a', 'b'];\n",
                    ['variables_only' => true],
                ),
            ],
        );
    }

    public function isCandidate(Tokens $tokens): bool
    {
        return $tokens->isAnyTokenKindsFound([\T_ARRAY, CT::T_ARRAY_BRACKET_OPEN]);
    }

    protected function createConfigurationDefinition(): FixerConfigurationResolverInterface
    {
        return new FixerConfigurationResolver([
            (new FixerOptionBuilder('min_items', 'Minimum number of elements an array must contain to be split across multiple lines.'))
                ->setAllowedTypes(['int'])
                ->setDefault(2)
                ->getOption(),
            (new FixerOptionBuilder('variables_only', 'Whether to only split arrays in an assignment-like position (assigned to a variable, returned, or yielded), leaving e.g. arrays passed as arguments untouched.'))
                ->setAllowedTypes(['bool'])
                ->setDefault(false)
                ->getOption(),
        ]);
    }

    protected function applyFix(\SplFileInfo $file, Tokens $tokens): void
    {
        $minItems = $this->configuration['min_items'];
        $variablesOnly = $this->configuration['variables_only'];

        for ($index = 0; $index < $tokens->count(); ++$index) {
            $token = $tokens[$index];

            if ($token->isGivenKind(\T_ARRAY)) {
                $arrayStartIndex = $index;
                $openIndex = $tokens->getNextTokenOfKind($index, ['(']);
                $closeIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS, $openIndex);
            } elseif ($token->isGivenKind(CT::T_ARRAY_BRACKET_OPEN)) {
                $arrayStartIndex = $index;
                $openIndex = $index;
                $closeIndex = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_ARRAY_BRACKET, $index);
            } else {
                continue;
            }

            if ($variablesOnly && !$this->isAssignedOrReturnedArray($tokens, $arrayStartIndex)) {
                continue;
            }

            $elementCount = $this->getElementCount($tokens, $openIndex, $closeIndex);

            if (null === $elementCount || $elementCount < 1 || $elementCount < $minItems) {
                continue;
            }

            $this->makeMultiline($tokens, $openIndex, $closeIndex);
        }
    }

    /**
     * Whether the array sits in an assignment-like position: right-hand side of an
     * assignment (variable, property, static property, …), or directly returned or yielded.
     */
    private function isAssignedOrReturnedArray(Tokens $tokens, int $arrayStartIndex): bool
    {
        $prevIndex = $tokens->getPrevMeaningfulToken($arrayStartIndex);

        return null !== $prevIndex
            && ($tokens[$prevIndex]->equals('=') || $tokens[$prevIndex]->isGivenKind([\T_RETURN, \T_YIELD]));
    }

    /**
     * Count the top-level elements of an array, skipping nested structures.
     *
     * Returns null when the array contains comments, in which case the existing
     * formatting must be preserved and the array left untouched.
     */
    private function getElementCount(Tokens $tokens, int $openIndex, int $closeIndex): ?int
    {
        $commaCount = 0;
        $hasContent = false;
        $lastMeaningfulIsComma = false;

        for ($index = $openIndex + 1; $index < $closeIndex; ++$index) {
            $token = $tokens[$index];

            if ($token->isComment()) {
                return null;
            }

            if ($token->isWhitespace()) {
                continue;
            }

            $hasContent = true;

            if ($token->equals(',')) {
                ++$commaCount;
                $lastMeaningfulIsComma = true;

                continue;
            }

            $lastMeaningfulIsComma = false;

            if ($token->equals('(')) {
                $index = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_PARENTHESIS, $index);
            } elseif ($token->isGivenKind(CT::T_ARRAY_BRACKET_OPEN)) {
                $index = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_ARRAY_BRACKET, $index);
            } elseif ($token->equals('{')) {
                $index = $tokens->findBlockEnd(Tokens::BLOCK_TYPE_BRACE, $index);
            }
        }

        if (!$hasContent) {
            return 0;
        }

        return $lastMeaningfulIsComma ? $commaCount : $commaCount + 1;
    }

    private function makeMultiline(Tokens $tokens, int $openIndex, int $closeIndex): void
    {
        $lineEnding = $this->whitespacesConfig->getLineEnding();
        $baseIndent = $this->detectIndent($tokens, $openIndex);
        $itemIndent = $baseIndent.$this->whitespacesConfig->getIndent();

        // ensure a newline right before the closing bracket
        $beforeEndIndex = $tokens[$closeIndex - 1]->isWhitespace() ? $closeIndex - 1 : $closeIndex;

        if ($tokens->ensureWhitespaceAtIndex($beforeEndIndex, 0, $lineEnding.$baseIndent)) {
            ++$closeIndex;
        }

        for ($index = $closeIndex - 1; $index > $openIndex; --$index) {
            $token = $tokens[$index];

            if ($token->equals(')')) {
                $index = $tokens->findBlockStart(Tokens::BLOCK_TYPE_PARENTHESIS, $index);

                continue;
            }

            if ($token->isGivenKind(CT::T_ARRAY_BRACKET_CLOSE)) {
                $index = $tokens->findBlockStart(Tokens::BLOCK_TYPE_ARRAY_BRACKET, $index);

                continue;
            }

            if ($token->equals('}')) {
                $index = $tokens->findBlockStart(Tokens::BLOCK_TYPE_BRACE, $index);

                continue;
            }

            if ($token->equals(',')) {
                // keep a trailing comma glued to the closing bracket line
                if ($tokens->getNextMeaningfulToken($index) === $closeIndex) {
                    continue;
                }

                $tokens->ensureWhitespaceAtIndex($index + 1, 0, $lineEnding.$itemIndent);
            }
        }

        // ensure a newline right after the opening bracket
        $tokens->ensureWhitespaceAtIndex($openIndex + 1, 0, $lineEnding.$itemIndent);
    }

    /**
     * Detect the indentation of the line on which the array opening is located.
     */
    private function detectIndent(Tokens $tokens, int $index): string
    {
        $searchIndex = $index;

        do {
            $prevWhitespaceIndex = $tokens->getPrevTokenOfKind(
                $searchIndex,
                [[\T_ENCAPSED_AND_WHITESPACE], [\T_INLINE_HTML], [\T_WHITESPACE]],
            );

            $searchIndex = $prevWhitespaceIndex;
        } while (null !== $prevWhitespaceIndex && !str_contains($tokens[$prevWhitespaceIndex]->getContent(), "\n"));

        if (null === $prevWhitespaceIndex || !$tokens[$prevWhitespaceIndex]->isGivenKind(\T_WHITESPACE)) {
            return '';
        }

        $content = $tokens[$prevWhitespaceIndex]->getContent();
        $lastNewlineIndex = strrpos($content, "\n");

        return false === $lastNewlineIndex ? $content : substr($content, $lastNewlineIndex + 1);
    }
}
