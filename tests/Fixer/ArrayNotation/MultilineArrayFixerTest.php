namespace PhpCsFixer\Tests\Fixer\ArrayNotation;

use PhpCsFixer\Fixer\ArrayNotation\MultilineArrayFixer;
use PhpCsFixer\Tests\Test\AbstractFixerTestCase;
use PhpCsFixer\WhitespacesFixerConfig;
use PHPUnit\Framework\Attributes\CoversClass;
use PHPUnit\Framework\Attributes\DataProvider;

/**
 * @internal
 *
 * @covers \PhpCsFixer\Fixer\ArrayNotation\MultilineArrayFixer
 *
 * @extends AbstractFixerTestCase<\PhpCsFixer\Fixer\ArrayNotation\MultilineArrayFixer>
 *
 * @phpstan-import-type _AutogeneratedInputConfiguration from \PhpCsFixer\Fixer\ArrayNotation\MultilineArrayFixer
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
#[CoversClass(MultilineArrayFixer::class)]
final class MultilineArrayFixerTest extends AbstractFixerTestCase
{
    /**
     * @dataProvider provideFixCases
     *
     * @param _AutogeneratedInputConfiguration $configuration
     */
    #[DataProvider('provideFixCases')]
    public function testFix(string $expected, ?string $input = null, ?array $configuration = null): void
    {
        if (null !== $configuration) {
            $this->fixer->configure($configuration);
        }

        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<string, array{0: string, 1?: null|string, 2?: _AutogeneratedInputConfiguration}>
     */
    public static function provideFixCases(): iterable
    {
        yield 'multiple elements get split' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b',
                    'c'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a', 'b', 'c'];
                EOD,
        ];

        yield 'single element is left untouched' => [
            <<<'EOD'
                <?php
                $foo = ['a'];
                EOD,
        ];

        yield 'empty array is left untouched' => [
            <<<'EOD'
                <?php
                $foo = [];
                EOD,
        ];

        yield 'keyed elements get split' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a' => 1,
                    'b' => 2
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a' => 1, 'b' => 2];
                EOD,
        ];

        yield 'old style array syntax' => [
            <<<'EOD'
                <?php
                $foo = array(
                    'a',
                    'b'
                );
                EOD,
            <<<'EOD'
                <?php
                $foo = array('a', 'b');
                EOD,
        ];

        yield 'trailing comma is kept' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b',
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a', 'b',];
                EOD,
        ];

        yield 'already multiline gets normalized' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b',
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = [
                  'a',
                        'b',
                ];
                EOD,
        ];

        yield 'indentation is kept' => [
            <<<'EOD'
                <?php
                class C {
                    public function f() {
                        $foo = [
                            'a',
                            'b'
                        ];
                    }
                }
                EOD,
            <<<'EOD'
                <?php
                class C {
                    public function f() {
                        $foo = ['a', 'b'];
                    }
                }
                EOD,
        ];

        yield 'nested arrays are split' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b',
                    [
                        'c',
                        'd'
                    ]
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a', 'b', ['c', 'd']];
                EOD,
        ];

        yield 'commas of nested calls are not counted' => [
            <<<'EOD'
                <?php
                $foo = [foo(1, 2)];
                EOD,
        ];

        yield 'nested call is kept on one line' => [
            <<<'EOD'
                <?php
                $foo = [
                    foo(1, 2),
                    'b'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = [foo(1, 2), 'b'];
                EOD,
        ];

        yield 'closure braces are skipped when counting and splitting' => [
            <<<'EOD'
                <?php
                $foo = [
                    function () { return 1; },
                    'b'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = [function () { return 1; }, 'b'];
                EOD,
        ];

        yield 'closure braces are not counted as elements' => [
            <<<'EOD'
                <?php
                $foo = [function () { return 1; }];
                EOD,
        ];

        yield 'arrays with comments are left untouched' => [
            <<<'EOD'
                <?php
                $foo = [/* x */ 'a', 'b'];
                EOD,
        ];

        yield 'min_items leaves smaller arrays untouched' => [
            <<<'EOD'
                <?php
                $foo = ['a', 'b', 'c'];
                EOD,
            null,
            ['min_items' => 4],
        ];

        yield 'min_items splits arrays at threshold' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b',
                    'c'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a', 'b', 'c'];
                EOD,
            ['min_items' => 3],
        ];

        yield 'min_items of one splits single element arrays' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a'];
                EOD,
            ['min_items' => 1],
        ];

        yield 'variables_only splits assigned arrays' => [
            <<<'EOD'
                <?php
                $foo = [
                    'a',
                    'b'
                ];
                EOD,
            <<<'EOD'
                <?php
                $foo = ['a', 'b'];
                EOD,
            ['variables_only' => true],
        ];

        yield 'variables_only splits returned arrays' => [
            <<<'EOD'
                <?php
                function f() {
                    return [
                        'a',
                        'b'
                    ];
                }
                EOD,
            <<<'EOD'
                <?php
                function f() {
                    return ['a', 'b'];
                }
                EOD,
            ['variables_only' => true],
        ];

        yield 'variables_only splits yielded arrays' => [
            <<<'EOD'
                <?php
                function f() {
                    yield [
                        'a',
                        'b'
                    ];
                }
                EOD,
            <<<'EOD'
                <?php
                function f() {
                    yield ['a', 'b'];
                }
                EOD,
            ['variables_only' => true],
        ];

        yield 'variables_only leaves argument arrays untouched' => [
            <<<'EOD'
                <?php
                bar(['a', 'b']);
                EOD,
            null,
            ['variables_only' => true],
        ];

        yield 'variables_only with mixed usage' => [
            <<<'EOD'
                <?php
                bar(['a', 'b']);
                $foo = [
                    'a',
                    'b'
                ];
                EOD,
            <<<'EOD'
                <?php
                bar(['a', 'b']);
                $foo = ['a', 'b'];
                EOD,
            ['variables_only' => true],
        ];
    }

    /**
     * @dataProvider provideWithWhitespacesConfigCases
     */
    #[DataProvider('provideWithWhitespacesConfigCases')]
    public function testWithWhitespacesConfig(string $expected, ?string $input = null): void
    {
        $this->fixer->setWhitespacesConfig(new WhitespacesFixerConfig("\t"));

        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<int, array{0: string, 1?: null|string}>
     */
    public static function provideWithWhitespacesConfigCases(): iterable
    {
        yield [
            "<?php\n\$foo = [\n\t'a',\n\t'b'\n];\n",
            "<?php\n\$foo = ['a', 'b'];\n",
        ];
    }
}
