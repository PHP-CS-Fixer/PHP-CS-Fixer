namespace PhpCsFixer\Tests\Fixer\Whitespace;

use PhpCsFixer\ConfigurationException\InvalidFixerConfigurationException;
use PhpCsFixer\Tests\Test\AbstractFixerTestCase;
use PhpCsFixer\WhitespacesFixerConfig;

/**
 * @internal
 *
 * @covers \PhpCsFixer\Fixer\Whitespace\BlankLineAfterStatementFixer
 *
 * @extends AbstractFixerTestCase<\PhpCsFixer\Fixer\Whitespace\BlankLineAfterStatementFixer>
 *
 * @author Andreas Möller <am@localheinz.com>
 *
 * @phpstan-import-type _AutogeneratedInputConfiguration from \PhpCsFixer\Fixer\Whitespace\BlankLineAfterStatementFixer
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class BlankLineAfterStatementFixerTest extends AbstractFixerTestCase
{
    /**
     * @dataProvider provideInvalidConfigurationCases
     *
     * @param mixed $controlStatement
     */
    public function testInvalidConfiguration($controlStatement): void
    {
        $this->expectException(InvalidFixerConfigurationException::class);

        $this->fixer->configure([
            'statements' => [$controlStatement],
        ]);
    }

    /**
     * @return iterable<string, array{mixed}>
     */
    public static function provideInvalidConfigurationCases(): iterable
    {
        yield 'null' => [null];

        yield 'false' => [false];

        yield 'true' => [true];

        yield 'int' => [0];

        yield 'float' => [3.14];

        yield 'array' => [[]];

        yield 'object' => [new \stdClass()];

        yield 'unknown' => ['foo'];

        yield 'break is not an allowed value' => ['break'];

        yield 'return is not an allowed value' => ['return'];
    }

    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideFixCases
     */
    public function testFix(string $expected, ?string $input = null, array $configuration = [], ?WhitespacesFixerConfig $whitespacesConfig = null): void
    {
        $this->fixer->configure($configuration);
        $this->fixer->setWhitespacesConfig($whitespacesConfig ?? new WhitespacesFixerConfig());
        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<string, array{0: string, 1?: null|string, 2?: _AutogeneratedInputConfiguration, 3?: WhitespacesFixerConfig}>
     */
    public static function provideFixCases(): iterable
    {
        yield 'default - issue #9496 declare followed by use requires blank line' => [
            '<?php

declare(strict_types=1);

use Foo\Bar;

return [];
',
            '<?php

declare(strict_types=1);
use Foo\Bar;

return [];
',
        ];

        yield 'default - declare followed by code requires blank line' => [
            '<?php

declare(strict_types=1);

$foo = 1;
',
            '<?php

declare(strict_types=1);
$foo = 1;
',
        ];

        yield 'default - declare with existing blank line is unchanged' => [
            '<?php

declare(strict_types=1);

$foo = 1;
',
        ];

        yield 'default - declare as last meaningful token is unchanged' => [
            '<?php
declare(strict_types=1);',
        ];

        yield 'default - declare with block form requires blank line after closing brace' => [
            '<?php
declare(ticks=1) {
    $foo = 1;
}

$bar = 2;
',
            '<?php
declare(ticks=1) {
    $foo = 1;
}
$bar = 2;
',
        ];

        yield 'default - no fix when declare is not configured' => [
            '<?php
if (true) {
}
$foo = 1;
',
            null,
            ['statements' => []],
        ];

        yield 'default - leading single-line comment after declare keeps comment attached to next statement' => [
            '<?php
declare(strict_types=1);

// comment belongs to use
use Foo\Bar;
',
            '<?php
declare(strict_types=1);
// comment belongs to use
use Foo\Bar;
',
        ];

        yield 'default - trailing inline comment on declare line stays on that line, blank line goes after the comment' => [
            '<?php
declare(strict_types=1); // trailing

use Foo\Bar;
',
            '<?php
declare(strict_types=1); // trailing
use Foo\Bar;
',
        ];

        yield 'if - trailing inline comment on closing brace line stays on that line, blank line goes after the comment' => [
            '<?php
if (true) {
    $a = 1;
} // trailing

$b = 2;
',
            '<?php
if (true) {
    $a = 1;
} // trailing
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'if - trailing hash comment on closing brace line stays on that line, blank line goes after the comment' => [
            '<?php
if (true) {
    $a = 1;
} # trailing

$b = 2;
',
            '<?php
if (true) {
    $a = 1;
} # trailing
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'default - leading docblock after declare keeps docblock attached to next statement' => [
            '<?php
declare(strict_types=1);

/** doc for Foo */
use Foo\Bar;
',
            '<?php
declare(strict_types=1);
/** doc for Foo */
use Foo\Bar;
',
        ];

        yield 'if - blank line inserted after closing brace' => [
            '<?php
if (true) {
    $a = 1;
}

$b = 2;
',
            '<?php
if (true) {
    $a = 1;
}
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'if - no blank line when next token is closing brace of enclosing scope' => [
            '<?php
function foo() {
    if (true) {
        $a = 1;
    }
}
',
            null,
            ['statements' => ['if']],
        ];

        yield 'if-elseif-else - blank line inserted after the end of the whole chain' => [
            '<?php
if (1 === 1) {
    $a = 1;
} elseif (2 === 2) {
    $a = 2;
} else {
    $a = 3;
}

$b = 2;
',
            '<?php
if (1 === 1) {
    $a = 1;
} elseif (2 === 2) {
    $a = 2;
} else {
    $a = 3;
}
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'if-else if - two-token else if variant walks the whole chain' => [
            '<?php
if (1 === 1) {
    $a = 1;
} else if (2 === 2) {
    $a = 2;
} else {
    $a = 3;
}

$b = 2;
',
            '<?php
if (1 === 1) {
    $a = 1;
} else if (2 === 2) {
    $a = 2;
} else {
    $a = 3;
}
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'switch - blank line inserted after closing brace' => [
            '<?php
switch ($a) {
    case 1:
        break;
}

$b = 2;
',
            '<?php
switch ($a) {
    case 1:
        break;
}
$b = 2;
',
            ['statements' => ['switch']],
        ];

        yield 'for - blank line inserted after closing brace' => [
            '<?php
for ($i = 0; $i < 3; ++$i) {
    $a = $i;
}

$b = 2;
',
            '<?php
for ($i = 0; $i < 3; ++$i) {
    $a = $i;
}
$b = 2;
',
            ['statements' => ['for']],
        ];

        yield 'foreach - blank line inserted after closing brace' => [
            '<?php
foreach ($arr as $v) {
    $a = $v;
}

$b = 2;
',
            '<?php
foreach ($arr as $v) {
    $a = $v;
}
$b = 2;
',
            ['statements' => ['foreach']],
        ];

        yield 'while - blank line inserted after closing brace' => [
            '<?php
while ($a) {
    --$a;
}

$b = 2;
',
            '<?php
while ($a) {
    --$a;
}
$b = 2;
',
            ['statements' => ['while']],
        ];

        yield 'do-while - blank line inserted after trailing semicolon, not after inner brace' => [
            '<?php
do {
    --$a;
} while ($a > 0);

$b = 2;
',
            '<?php
do {
    --$a;
} while ($a > 0);
$b = 2;
',
            ['statements' => ['do']],
        ];

        yield 'do-while - enabling while alone must not fix the do/while construct' => [
            '<?php
do {
    --$a;
} while ($a > 0);
$b = 2;
',
            null,
            ['statements' => ['while']],
        ];

        yield 'try-catch - blank line inserted after the end of the whole chain' => [
            '<?php
try {
    $a = 1;
} catch (\Throwable $t) {
    $a = 0;
}

$b = 2;
',
            '<?php
try {
    $a = 1;
} catch (\Throwable $t) {
    $a = 0;
}
$b = 2;
',
            ['statements' => ['try']],
        ];

        yield 'try-catch-finally - blank line inserted after the end of the whole chain' => [
            '<?php
try {
    $a = 1;
} catch (\Throwable $t) {
    $a = 0;
} finally {
    $a = -1;
}

$b = 2;
',
            '<?php
try {
    $a = 1;
} catch (\Throwable $t) {
    $a = 0;
} finally {
    $a = -1;
}
$b = 2;
',
            ['statements' => ['try']],
        ];

        yield 'nested if with declare default is unaffected' => [
            '<?php
if (true) {
    if (false) {
        $a = 1;
    }
}
',
            null,
            ['statements' => ['declare']],
        ];

        yield 'multiple statements configured - declare and if both fixed' => [
            '<?php

declare(strict_types=1);

if (true) {
    $a = 1;
}

$b = 2;
',
            '<?php

declare(strict_types=1);
if (true) {
    $a = 1;
}
$b = 2;
',
            ['statements' => ['declare', 'if']],
        ];

        yield 'alternative syntax - if/endif is out of scope and left unchanged' => [
            '<?php
if ($a):
    $b = 1;
endif;
$c = 2;
',
            null,
            ['statements' => ['if']],
        ];

        yield 'alternative syntax - for/endfor is out of scope and left unchanged' => [
            '<?php
for ($i = 0; $i < 3; ++$i):
    $a = $i;
endfor;
$b = 2;
',
            null,
            ['statements' => ['for']],
        ];

        yield 'alternative syntax - foreach/endforeach is out of scope and left unchanged' => [
            '<?php
foreach ($arr as $v):
    $a = $v;
endforeach;
$b = 2;
',
            null,
            ['statements' => ['foreach']],
        ];

        yield 'alternative syntax - while/endwhile is out of scope and left unchanged' => [
            '<?php
while ($a):
    --$a;
endwhile;
$b = 2;
',
            null,
            ['statements' => ['while']],
        ];

        yield 'alternative syntax - switch/endswitch is out of scope and left unchanged' => [
            '<?php
switch ($a):
    case 1:
        break;
endswitch;
$b = 2;
',
            null,
            ['statements' => ['switch']],
        ];

        yield 'single-statement body - if without braces gets blank line after the terminating semicolon' => [
            '<?php
if ($a) $b = 1;

$c = 2;
',
            '<?php
if ($a) $b = 1;
$c = 2;
',
            ['statements' => ['if']],
        ];

        yield 'single-statement body - if/else without braces walks to end of else body' => [
            '<?php
if ($a) $b = 1; else $c = 2;

$d = 3;
',
            '<?php
if ($a) $b = 1; else $c = 2;
$d = 3;
',
            ['statements' => ['if']],
        ];

        yield 'single-statement body - while without braces' => [
            '<?php
while ($a) --$a;

$b = 2;
',
            '<?php
while ($a) --$a;
$b = 2;
',
            ['statements' => ['while']],
        ];

        yield 'single-statement body - for without braces' => [
            '<?php
for ($i = 0; $i < 3; ++$i) $a = $i;

$b = 2;
',
            '<?php
for ($i = 0; $i < 3; ++$i) $a = $i;
$b = 2;
',
            ['statements' => ['for']],
        ];

        yield 'single-statement body - foreach without braces' => [
            '<?php
foreach ($arr as $v) $a = $v;

$b = 2;
',
            '<?php
foreach ($arr as $v) $a = $v;
$b = 2;
',
            ['statements' => ['foreach']],
        ];

        yield 'empty body - if with empty braces still gets blank line when followed by a statement' => [
            '<?php
if (true) {}

$b = 2;
',
            '<?php
if (true) {}
$b = 2;
',
            ['statements' => ['if']],
        ];

        yield 'empty body - if with empty braces as last statement in function is unchanged' => [
            '<?php
function foo() {
    if (true) {}
}
',
            null,
            ['statements' => ['if']],
        ];

        yield 'empty body - try/catch with empty braces' => [
            '<?php
try {} catch (\Throwable $t) {}

$b = 2;
',
            '<?php
try {} catch (\Throwable $t) {}
$b = 2;
',
            ['statements' => ['try']],
        ];

        yield 'empty body - do/while with empty braces' => [
            '<?php
do {} while (true);

$b = 2;
',
            '<?php
do {} while (true);
$b = 2;
',
            ['statements' => ['do']],
        ];

        yield 'nested - try wrapping an if gets blank lines after both when configured for both' => [
            '<?php
try {
    if ($a) {
        $b = 1;
    }

    $c = 2;
} catch (\Throwable $t) {
    $d = 3;
}

$e = 4;
',
            '<?php
try {
    if ($a) {
        $b = 1;
    }
    $c = 2;
} catch (\Throwable $t) {
    $d = 3;
}
$e = 4;
',
            ['statements' => ['if', 'try']],
        ];

        yield 'nested - if wrapping a try gets blank lines after both when configured for both' => [
            '<?php
if ($a) {
    try {
        $b = 1;
    } catch (\Throwable $t) {
        $c = 2;
    }

    $d = 3;
}

$e = 4;
',
            '<?php
if ($a) {
    try {
        $b = 1;
    } catch (\Throwable $t) {
        $c = 2;
    }
    $d = 3;
}
$e = 4;
',
            ['statements' => ['if', 'try']],
        ];

        yield 'nested - foreach wrapping a switch' => [
            '<?php
foreach ($arr as $v) {
    switch ($v) {
        case 1:
            break;
    }

    $a = 1;
}

$b = 2;
',
            '<?php
foreach ($arr as $v) {
    switch ($v) {
        case 1:
            break;
    }
    $a = 1;
}
$b = 2;
',
            ['statements' => ['foreach', 'switch']],
        ];

        yield 'CRLF line endings preserved' => [
            "<?php\r\n\r\ndeclare(strict_types=1);\r\n\r\nuse Foo\\Bar;\r\n",
            "<?php\r\n\r\ndeclare(strict_types=1);\r\nuse Foo\\Bar;\r\n",
            [],
            new WhitespacesFixerConfig('    ', "\r\n"),
        ];
    }
}
