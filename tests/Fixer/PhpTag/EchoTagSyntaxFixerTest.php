namespace PhpCsFixer\Tests\Fixer\PhpTag;

use PhpCsFixer\Tests\Test\AbstractFixerTestCase;

/**
 * @internal
 *
 * @covers \PhpCsFixer\Fixer\PhpTag\EchoTagSyntaxFixer
 *
 * @extends AbstractFixerTestCase<\PhpCsFixer\Fixer\PhpTag\EchoTagSyntaxFixer>
 *
 * @author Michele Locati <michele@locati.it>
 *
 * @phpstan-import-type _AutogeneratedInputConfiguration from \PhpCsFixer\Fixer\PhpTag\EchoTagSyntaxFixer
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class EchoTagSyntaxFixerTest extends AbstractFixerTestCase
{
    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideFixCases
     */
    public function testFix(string $expected, ?string $input = null, array $configuration = []): void
    {
        $this->fixer->configure($configuration);

        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<int, array{0: string, 1?: null|string, 2?: _AutogeneratedInputConfiguration}>
     */
    public static function provideFixCases(): iterable
    {
        yield [
            '<?= \'Foo\';',
            '<?php echo \'Foo\';',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\';',
            '<?php print \'Foo\';',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\'; ?> PLAIN TEXT',
            '<?php echo \'Foo\'; ?> PLAIN TEXT',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\'; ?> PLAIN TEXT',
            '<?php print \'Foo\'; ?> PLAIN TEXT',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            'PLAIN TEXT<?= \'Foo\'; ?>',
            'PLAIN TEXT<?php echo \'Foo\'; ?>',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            'PLAIN TEXT<?= \'Foo\'; ?>',
            'PLAIN TEXT<?php print \'Foo\'; ?>',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\'; ?> <?= \'Bar\'; ?>',
            '<?php echo \'Foo\'; ?> <?php echo \'Bar\'; ?>',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\'; ?> <?= \'Bar\'; ?>',
            '<?php print \'Foo\'; ?> <?php echo \'Bar\'; ?>',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?php echo \'Foo\'; someThingElse();',
            null,
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            '<?= \'Foo\'; someThingElse();',
            '<?php echo \'Foo\'; someThingElse();',
            ['format' => 'short', 'shorten_simple_statements_only' => false],
        ];

        yield [
            '<?=/*this */ /** should be in the result*/ \'Foo\';',
            '<?php /*this */ /** should be in the result*/ echo \'Foo\';',
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            <<<'EOT'
                <?=/*comment*/
                  1
                ?>
                EOT,
            <<<'EOT'
                <?php /*comment*/ echo
                  1
                ?>
                EOT,
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            <<<'EOT'
                <?=/*comment*/ 1
                ?>
                EOT,
            <<<'EOT'
                <?php
                  /*comment*/ echo 1
                ?>
                EOT,
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        yield [
            <<<'EOT'
                <?=/*comment*/
                  1
                ?>
                EOT,
            <<<'EOT'
                <?php
                  /*comment*/
                  echo
                  1
                ?>
                EOT,
            ['format' => 'short', 'shorten_simple_statements_only' => true],
        ];

        $shortToLongFormatCases = [
            ['<?php <fn> 1;', '<?= 1;'],
            ['<?php <fn> 1;', '<?=1;'],
            ['<?php <fn> /**/1;', '<?=/**/1;'],
            ['<?php <fn> /**/ 1;', '<?=/**/ 1;'],
            ['<?php <fn> \'Foo\';', '<?= \'Foo\';'],
            ['<?php <fn> \'Foo\'; ?> PLAIN TEXT', '<?= \'Foo\'; ?> PLAIN TEXT'],
            ['PLAIN TEXT<?php <fn> \'Foo\'; ?>', 'PLAIN TEXT<?= \'Foo\'; ?>'],
            ['<?php <fn> \'Foo\'; ?> <?php <fn> \'Bar\'; ?>', '<?= \'Foo\'; ?> <?= \'Bar\'; ?>'],
            ['<?php <fn> foo();', '<?=foo();'],
        ];

        foreach (['echo', 'print'] as $function) {
            foreach ($shortToLongFormatCases as $case) {
                yield [
                    str_replace('<fn>', $function, $case[0]),
                    str_replace('<fn>', $function, $case[1]),
                    ['format' => 'long', 'long_function' => $function],
                ];
            }
        }

        yield [
            <<<'PHP'
                <?=// comment
                 'Foo';
                ?>
                PHP,
            <<<'PHP'
                <?php
                // comment
                echo 'Foo';
                ?>
                PHP,
            ['format' => 'short'],
        ];

        yield [
            <<<'EOT'
                <?php echo 1 ?>
                <?= 2 ?>
                EOT,
            <<<'EOT'
                <?php echo 1 ?>
                <?php echo 2 ?>
                EOT,
            ['format' => 'short', 'ignore_start_of_file' => true],
        ];

        yield [
            <<<'EOT'
                PLAIN TEXT<?= 1 ?>
                EOT,
            <<<'EOT'
                PLAIN TEXT<?php echo 1 ?>
                EOT,
            ['format' => 'short', 'ignore_start_of_file' => true],
        ];

        yield [
            <<<'EOT'
                <?php $foo = 1; ?>
                <?= 1 ?>
                EOT,
            <<<'EOT'
                <?php $foo = 1; ?>
                <?php echo 1 ?>
                EOT,
            ['format' => 'short', 'ignore_start_of_file' => true],
        ];

        yield [
            <<<'EOT'
                <?= 1 ?>
                <?= 2 ?>
                EOT,
            <<<'EOT'
                <?= 1 ?>
                <?php echo 2 ?>
                EOT,
            ['format' => 'short', 'ignore_start_of_file' => true],
        ];

        yield [
            <<<'EOT'
                PLAIN TEXT<?php echo 1 ?>
                EOT,
            <<<'EOT'
                PLAIN TEXT<?= 1 ?>
                EOT,
            ['format' => 'long', 'ignore_start_of_file' => true],
        ];

        yield [
            <<<'EOT'
                <?= 12 + 4; ?>
                <?php echo 123;
                EOT,
            <<<'EOT'
                <?php echo 12 + 4; ?>
                <?php echo 123;
                EOT,
            ['format' => 'short', 'ignore_tag_without_closing_tag' => true],
        ];

        yield [
            <<<'EOT'
                <?php echo 1+2; ?>
                <?= 123;
                EOT,
            <<<'EOT'
                <?= 1+2; ?>
                <?= 123;
                EOT,
            ['format' => 'long', 'ignore_tag_without_closing_tag' => true],
        ];

        yield [
            '<?php /* comment */ echo 12; ?><?= 13; ?>',
            '<?php /* comment */ echo 12; ?><?php echo 13; ?>',
            ['format' => 'short', 'shorten_tags_without_comments_only' => true],
        ];

        yield [
            '<?php /** comment */ echo 12; ?><?= 13; ?>',
            '<?php /** comment */ echo 12; ?><?php echo 13; ?>',
            ['format' => 'short', 'shorten_tags_without_comments_only' => true],
        ];

        yield [
            <<<'EOT'
                            '<?php
                            # comment
                            echo 12;
                            ?>
                            <?= 13; ?>',
                EOT,
            <<<'EOT'
                            '<?php
                            # comment
                            echo 12;
                            ?>
                            <?php echo 13; ?>',
                EOT,
            ['format' => 'short', 'shorten_tags_without_comments_only' => true],
        ];

        yield [
            <<<'EOT'
                            '<?php
                            // comment
                            echo 12;
                            ?>
                            <?= 13; ?>',
                EOT,
            <<<'EOT'
                            '<?php
                            // comment
                            echo 12;
                            ?>
                            <?php echo 13; ?>',
                EOT,
            ['format' => 'short', 'shorten_tags_without_comments_only' => true],
        ];
    }
}
