namespace PhpCsFixer\Tests\Fixer\Phpdoc;

use PhpCsFixer\ConfigurationException\InvalidFixerConfigurationException;
use PhpCsFixer\Fixer\Phpdoc\PhpdocTagCasingFixer;
use PhpCsFixer\Tests\Test\AbstractFixerTestCase;
use PHPUnit\Framework\Attributes\CoversClass;
use PHPUnit\Framework\Attributes\DataProvider;

/**
 * @internal
 *
 * @covers \PhpCsFixer\Fixer\Phpdoc\PhpdocTagCasingFixer
 *
 * @extends AbstractFixerTestCase<\PhpCsFixer\Fixer\Phpdoc\PhpdocTagCasingFixer>
 *
 * @phpstan-import-type _AutogeneratedInputConfiguration from \PhpCsFixer\Fixer\Phpdoc\PhpdocTagCasingFixer
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
#[CoversClass(PhpdocTagCasingFixer::class)]
final class PhpdocTagCasingFixerTest extends AbstractFixerTestCase
{
    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideFixCases
     */
    #[DataProvider('provideFixCases')]
    public function testFix(string $expected, ?string $input = null, array $configuration = []): void
    {
        $this->fixer->configure($configuration);

        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<int, array{0: string, 1?: null|string, 2?: _AutogeneratedInputConfiguration}>
     */
    public static function provideFixCases(): iterable
    {
        yield [
            '<?php /** @inheritDoc */',
            '<?php /** @inheritdoc */',
        ];

        yield [
            '<?php /** @inheritDoc */',
            '<?php /** @inheritdoc */',
            ['tags' => ['inheritDoc']],
        ];

        yield [
            '<?php /** @inheritdoc */',
            '<?php /** @inheritDoc */',
            ['tags' => ['inheritdoc']],
        ];

        yield [
            '<?php /** {@inheritDoc} */',
            '<?php /** {@inheritdoc} */',
        ];

        yield [
            '<?php /** {@inheritDoc} */',
            '<?php /** {@inheritdoc} */',
            ['tags' => ['inheritDoc']],
        ];

        yield [
            '<?php /** {@inheritdoc} */',
            '<?php /** {@inheritDoc} */',
            ['tags' => ['inheritdoc']],
        ];
    }

    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideInvalidConfigurationCases
     */
    #[DataProvider('provideInvalidConfigurationCases')]
    public function testInvalidConfiguration(array $configuration, string $message): void
    {
        $this->expectException(InvalidFixerConfigurationException::class);
        $this->expectExceptionMessage($message);

        $this->fixer->configure($configuration);
    }

    /**
     * @return iterable<string, array{_AutogeneratedInputConfiguration, string}>
     */
    public static function provideInvalidConfigurationCases(): iterable
    {
        yield 'tag containing comment-end sequence' => [
            ['tags' => ['foo*/']],
            '[phpdoc_tag_casing] Invalid configuration: Tag "foo*/" cannot be replaced by invalid tag "foo*/".',
        ];

        yield 'tag with whitespace' => [
            ['tags' => ['foo bar']],
            '[phpdoc_tag_casing] Invalid configuration: Tag "foo bar" cannot be replaced by invalid tag "foo bar".',
        ];
    }
}
