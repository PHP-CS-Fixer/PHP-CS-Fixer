    /**
     * @param array<string, mixed> $config
     *
     * @dataProvider provideInvalidConfigurationCases
     */
    public function testInvalidConfiguration(array $config, string $exceptionExpression): void
    {
        $this->expectException(InvalidFixerConfigurationException::class);
        $this->expectExceptionMessageMatches($exceptionExpression);

        $this->fixer->configure($config);
    }

    /**
     * @return iterable<string, array{array<string, mixed>, string}>
     */
    public static function provideInvalidConfigurationCases(): iterable
    {
        yield 'invalid option' => [
            ['foo' => true],
            '/^\[no_line_break_near_binary_operator\] Invalid configuration: The option "foo" does not exist\. Defined options are: "default_strategy", "operators"\.$/',
        ];

        yield 'wrong config type for operators' => [
            ['operators' => true],
            '/^\[no_line_break_near_binary_operator\] Invalid configuration: The option "operators" with value true is expected to be of type "array", but is of type "(bool|boolean)"\.$/',
        ];

        yield 'wrong config value for default strategy' => [
            ['default_strategy' => 'abc'],
            '/^\[no_line_break_near_binary_operator\] Invalid configuration: The option "default_strategy" with value "abc" is invalid. Accepted values are: "before", "after", "around", null\.$/',
        ];

        yield 'wrong config type for operators value' => [
            ['operators' => ['+' => 'abc']],
            '/^\[no_line_break_near_binary_operator\] Invalid configuration: Unexpected value for operator "\+", expected any of ".*", got "string#abc"\.$/',
        ];
    }

    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideFixCases
     */
    public function testFix(string $expected, ?string $input = null, array $configuration = []): void
    {
        $this->fixer->configure($configuration);
        $this->doTest($expected, $input);
    }

