namespace PhpCsFixer\Tests\Fixer\Operator;

use PhpCsFixer\ConfigurationException\InvalidFixerConfigurationException;
use PhpCsFixer\Tests\Test\AbstractFixerTestCase;

/**
 * @internal
 *
 * @covers \PhpCsFixer\Fixer\Operator\NoLineBreakAroundBinaryOperatorFixer
 *
 * @extends AbstractFixerTestCase<\PhpCsFixer\Fixer\Operator\NoLineBreakAroundBinaryOperatorFixer>
 *
 * @author Maksim Spirkov <spirkov.2001@mail.ru>
 *
 * @phpstan-import-type _AutogeneratedInputConfiguration from \PhpCsFixer\Fixer\Operator\NoLineBreakAroundBinaryOperatorFixer
 *
 * @no-named-arguments Parameter names are not covered by the backward compatibility promise.
 */
final class NoLineBreakAroundBinaryOperatorFixerTest extends AbstractFixerTestCase
{
    /**
     * @param array<string, mixed> $config
     *
     * @dataProvider provideInvalidConfigurationCases
     */
    public function testInvalidConfiguration(array $config, string $exceptionExpression): void
    {
        $this->expectException(InvalidFixerConfigurationException::class);
        $this->expectExceptionMessageMatches($exceptionExpression);

        $this->fixer->configure($config);
    }

    /**
     * @return iterable<string, array{array<string, mixed>, string}>
     */
    public static function provideInvalidConfigurationCases(): iterable
    {
        yield 'wrong config type for ignored_operators' => [
            ['ignored_operators' => true],
            '/^\[no_line_break_around_binary_operator\] Invalid configuration: The option "ignored_operators" with value true is expected to be of type "string\[\]", but is of type "bool"\.$/',
        ];
    }

    /**
     * @param _AutogeneratedInputConfiguration $configuration
     *
     * @dataProvider provideFixCases
     */
    public function testFix(string $expected, ?string $input = null, array $configuration = []): void
    {
        $this->fixer->configure($configuration);
        $this->doTest($expected, $input);
    }

    /**
     * @return iterable<string, array{0: string, 1?: null|string, 2?: _AutogeneratedInputConfiguration}>
     */
    public static function provideFixCases(): iterable
    {
        yield 'correct code' => [
            '<?php
                $var1 = [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
                $var2 ??= new stdClass();
                $var3 *= 123;
                $var4 /= 12456767;
                $var5 .= "Text";
                $var6 &= true;
                $var7 |= false;
                $var8 = $var5 . "Text2";
            ',
        ];

        yield 'incorrect code' => [
            '<?php
                $var1 = [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
                $var2 ??= new stdClass();
                $var3 *= 123;
                $var4 /= 12456767;
                $var5 .= "Text";
                $var6 &= true;
                $var7 |= false;
                $var8 = $var5 . "Text2";
            ',
            '<?php
                $var1 =
                [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
                $var2
                    ??= new stdClass();
                $var3 *=
                    123;
                $var4 /=
                12456767;
                $var5
                .= "Text";
                $var6 &=
                    true;
                $var7
                |= false;
                $var8 = $var5 .
                "Text2";
            ',
        ];

        yield 'multiline array elements' => [
            '<?php
                $var1 = [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
            ',
            '<?php
                $var1 = [
                    "foo"
                        => "bar",
                    "bar"
                    => "baz",
                ];
            ',
        ];

        yield 'operators with comments' => [
            '<?php
                $var1 = "Text" . // Some comment
                    "Text2";
                $var2 = 1234 + // Some comment 2
                    4567;
            ',
        ];

        yield 'incorrect code with custom config' => [
            '<?php
                $var1 =
                [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
                $var2 ??= new stdClass();
                $var3 *=
                    123;
                $var4 /=
                12456767;
                $var5
                .= "Text";
                $var6 &=
                    true;
                $var7
                |= false;
                $var8 = $var5 .
                "Text2";
            ',
            '<?php
                $var1 =
                [
                    "foo" => "bar",
                    "bar" => "baz",
                ];
                $var2
                    ??= new stdClass();
                $var3 *=
                    123;
                $var4 /=
                12456767;
                $var5
                .= "Text";
                $var6 &=
                    true;
                $var7
                |= false;
                $var8 = $var5 .
                "Text2";
            ',
            ['ignored_operators' => ['=', '*=', '/=', '.=', '&=', '|=', '.']],
        ];
    }
}
